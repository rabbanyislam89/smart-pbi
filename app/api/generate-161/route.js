import { GoogleGenerativeAI } from "@google/generative-ai";
import { NextResponse } from "next/server";

const genAI = new GoogleGenerativeAI(process.env.GEMINI_API_KEY);

export async function POST(req) {
  try {
    // 🔥 আপডেট: ফ্রন্টএন্ড থেকে report এর পাশাপাশি note-ও রিসিভ করা হচ্ছে
    const { report, note } = await req.json();

    if (!report) {
      return NextResponse.json({ error: "Report content is missing" }, { status: 400 });
    }

    const model = genAI.getGenerativeModel({ model: "gemini-flash-latest" });

    // ==================================================================================
    // 🔥 ১৬১ জবানবন্দি জেনারেটর (Consistency + Page Break + Word Friendly)
    // ১. রিপোর্টের তথ্যের সাথে ১০০% মিল রাখবে।
    // ২. page-break-after ব্যবহার করা হয়েছে আলাদা পেজের জন্য।
    // ৩. <p> ট্যাগ ব্যবহার করা হয়েছে যাতে লেখা একটার গায়ে একটা না লাগে।
    // ==================================================================================

    const prompt = `
      ভূমিকা: তুমি একজন অভিজ্ঞ পিবিআই তদন্ত কর্মকর্তা। নিচে একটি তদন্ত প্রতিবেদন (Report) দেওয়া হলো। তোমার কাজ হলো এই রিপোর্টের তথ্যের ভিত্তিতে ফৌজদারী কার্যবিধির ১৬১ ধারায় সাক্ষীদের জবানবন্দি তৈরি করা।

      ইনপুট রিপোর্ট:
      """
      ${report}
      """

      অফিসারের বিশেষ নোট (IO Note): 
      """
      ${note || "কোনো বিশেষ নোট নেই। রিপোর্টের ভিত্তিতে স্বাভাবিক জবানবন্দি তৈরি করো।"}
      """

      কঠোর নির্দেশাবলী (আদালতের জন্য):
      ১. **সত্যতা ও মিল:** জবানবন্দির তথ্য অবশ্যই উপরের রিপোর্টের তথ্যের সাথে হুবহু মিলতে হবে। ঘটনার তারিখ, সময়, টাকার পরিমাণ, ঘটনাস্থল এবং আসামীর নাম যেন রিপোর্টের সাথে ১ চুলও ভিন্ন না হয়।
      ২. **অফিসারের নোট অনুসরণ:** উপরে দেওয়া "অফিসারের বিশেষ নোট" এ যদি কোনো সাক্ষীর নির্দিষ্ট জবানবন্দির কথা বলা থাকে, তবে তা সর্বোচ্চ গুরুত্ব দিয়ে সেই অনুযায়ী জবানবন্দি সাজাবে।
      ৩. **ফরম্যাট:** MS Word-এ কপি করার জন্য শুধুমাত্র HTML <p> ট্যাগ ব্যবহার করবে। কোনো <div> ব্যবহার করবে না।
      ৪. **পেজ ব্রেক:** প্রতিটি সাক্ষীর জবানবন্দি শেষ হওয়ার পর <br style="page-break-after: always;"> ট্যাগ দিবে।
      ৫. **ফাঁকা জায়গা:** প্যারাগ্রাফের মাঝে ফাঁকা জায়গার জন্য <p>&nbsp;</p> ব্যবহার করবে।

      আউটপুট স্ট্রাকচার (প্রতিটি সাক্ষীর জন্য হুবহু এই ফরম্যাট):

      <p><b>১৬১ ধারায় জবানবন্দি - সাক্ষী নং ১</b></p>
      <p>&nbsp;</p>

      <p><b>নাম:</b> [রিপোর্ট থেকে নাম]</p>
      <p><b>পিতা/স্বামী:</b> [রিপোর্ট থেকে]</p>
      <p><b>সাং:</b> [রিপোর্ট থেকে]</p>
      <p><b>পেশা:</b> [পেশা]</p>
      <p>&nbsp;</p>

      <p><b>জবানবন্দি:</b></p>
      <p>
        "আমি অত্র মামলার ঘটনার বিষয়ে অবগত আছি। গত [রিপোর্টে উল্লিখিত ঘটনার তারিখ] তারিখে [ঘটনাস্থল]-এ ঘটনাটি ঘটে। আমি জানি যে, [রিপোর্টের তথ্যের ভিত্তিতে ঘটনার সংক্ষিপ্ত বর্ণনা যা রিপোর্টের সাথে মিলে]। আসামী [আসামীর নাম] বাদীর সাথে [প্রতারণা/মারামারি/ঘটনা] করিয়াছে। আমি এই ঘটনার সুষ্ঠু বিচার চাই।"
      </p>
      <p>&nbsp;</p>

      <p>ইহা আমার জবানবন্দি, আমাকে পড়িয়া শুনাইলে আমি শুদ্ধ মর্মে স্বীকার করিয়া সহি সম্পাদন করলাম।</p>
      <p>&nbsp;</p>
      <p>&nbsp;</p>

      <p>_________________________</p>
      <p><b>সাক্ষীর স্বাক্ষর/টিপসহি</b></p>
      <p>তারিখ: [রিপোর্টে উল্লিখিত তদন্তের তারিখ]</p>

      <br style="page-break-after: always;">


      <p><b>১৬১ ধারায় জবানবন্দি - সাক্ষী নং ২</b></p>
      <p>&nbsp;</p>
      <p><b>নাম:</b> [নাম]</p>
      ... (বাকি তথ্য একই ফরম্যাটে)

      <br style="page-break-after: always;">
    `;

    const result = await model.generateContent(prompt);
    
    // ক্লিনআপ
    let statementText = result.response.text().replace(/```html/g, "").replace(/```/g, "");

    return NextResponse.json({ statement: statementText });

  } catch (error) {
    console.error("Error:", error);
    return NextResponse.json({ error: "সার্ভারে সমস্যা হয়েছে।" }, { status: 500 });
  }
}