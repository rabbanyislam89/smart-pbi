import { GoogleGenerativeAI } from "@google/generative-ai";
import { NextResponse } from "next/server";

const genAI = new GoogleGenerativeAI(process.env.GEMINI_API_KEY);

export async function POST(req) {
  try {
    const { report, note } = await req.json();

    if (!report) {
      return NextResponse.json({ error: "Report content is missing" }, { status: 400 });
    }

    const model = genAI.getGenerativeModel({ model: "gemini-flash-latest" });

    // ==================================================================================
    // 🔥 ১৬১ জবানবন্দি জেনারেটর (Dynamic Perspective & Master Logic)
    // ==================================================================================

    const prompt = `
      ভূমিকা: তুমি পিবিআই (PBI)-এর একজন অত্যন্ত অভিজ্ঞ ও চৌকস তদন্তকারী কর্মকর্তা। 
      কাজ: নিচে তোমাকে একটি চূড়ান্ত 'তদন্ত প্রতিবেদন' (Final Report) দেওয়া হলো। তোমার কাজ হলো এই রিপোর্টের তথ্যের ওপর ভিত্তি করে ফৌজদারী কার্যবিধির ১৬১ ধারায় সাক্ষীদের জবানবন্দি তৈরি করা।

      ইনপুট রিপোর্ট (ফাইনাল রিপোর্ট):
      """
      ${report}
      """

      অফিসারের বিশেষ নোট (IO Note): 
      """
      ${note || "কোনো বিশেষ নোট নেই। রিপোর্টের ওপর ভিত্তি করে মামলার প্রধান ২-৩ জন সাক্ষীর জবানবন্দি তৈরি করো (যাদের মধ্যে ভিকটিম এবং নিরপেক্ষ সাক্ষী থাকবে)।"}
      """

      কঠোর নির্দেশাবলী (আদালতের জন্য):
      ১. **ভাষাগত নির্দেশ (Strict Rule):** পুরো জবানবন্দি ১০০% প্রমিত 'চলিত ভাষায়' (যেমন: দেখেছি, মেরেছে, হয়েছে, যায়নি) লিখতে হবে। সাধু ভাষা ব্যবহার করা যাবে না।
      
      ২. **স্বাভাবিক বৈচিত্র্য (Perspective Variation - Most Important):** সব সাক্ষীর জবানবন্দি কোনোভাবেই হুবহু একরকম হবে না। 
         - **ভিকটিম/আহত সাক্ষী:** সে নিজের ওপর হওয়া আঘাতের বিস্তারিত বর্ণনা দেবে (যেমন: "আমাকে ১নং আসামী রড দিয়ে ডান হাতে আঘাত করে হাড় ভেঙ্গে দেয়...")।
         - **পরিবারের সদস্য/ঘনিষ্ঠ সাক্ষী:** সে বলবে "আমি চিৎকার শুনে দৌড়ে গিয়ে দেখলাম..." এবং সে আসামীদের নাম ধরে বিস্তারিত বলবে।
         - **নিরপেক্ষ সাক্ষী (প্রতিবেশী/পথচারী):** সে একটু দূর থেকে দেখার ভঙ্গি করবে। সে বলবে "আমি গ্যাঞ্জাম শুনে এগিয়ে যাই এবং দেখি যে মারামারি হচ্ছে..."। সে হয়তো সব আসামীর হাতে কী অস্ত্র ছিল তা হুবহু নাও বলতে পারে, তবে মূল ঘটনা সমর্থন করবে। এতে জবানবন্দিগুলো মানুষের কথার মতো স্বাভাবিক ও বিশ্বাসযোগ্য মনে হবে।

      ৩. **প্রমাণিত বনাম অপ্রমাণিত (The Secret Sauce):** রিপোর্টের ১৬ নম্বর পয়েন্টে (মতামত) যে অভিযোগগুলো (যেমন: ৩৭৯ চুরি, ৩৫৪ শ্লীলতাহানি, ৩০৭ হত্যাচেষ্টা) 'অপ্রমাণিত' বলা হয়েছে, সাক্ষীরা সুকৌশলে তা অস্বীকার করবে। (যেমন: "আমি সেখানে কোনো স্বর্ণ ছিনতাই বা শ্লীলতাহানির ঘটনা দেখিনি")। 
      
      ৪. **অফিসারের নোট (Priority):** নোটে যদি কোনো নির্দিষ্ট সাক্ষীর নাম ও তার জবানবন্দির সারাংশ দেওয়া থাকে, তবে হুবহু সেই নির্দেশ পালন করবে।

      ৫. **ফরম্যাট ও পেজ ব্রেক:** MS Word-এ পেস্ট করার জন্য কোনো <div>, &nbsp; বা Tab ব্যবহার করবে না। প্রতিটি সাক্ষীর জবানবন্দি শেষ হওয়ার পর <br style="page-break-after: always;"> ট্যাগ দিবে।

      আউটপুট স্ট্রাকচার (তোমার আউটপুট হুবহু নিচের নমুনার মতো হতে হবে):

      [আদর্শ নমুনা ফরম্যাট - হুবহু এটি অনুসরণ করবে]:
      <p><b>সাক্ষীর জবানবন্দি</b></p>
      <p><b>সূত্রঃ-</b> [জেলা] জেলার [থানা] থানার [এফআইআর/সিআর] নং-[নম্বর], তারিখ-[তারিখ] খ্রিঃ, ধারা-[ধারাগুলো] সংক্রান্তে সাক্ষী [সাক্ষীর নাম] ([বয়স]), পিতা/স্বামী-[নাম], সাং-[ঠিকানা], থানা-[থানা], জেলা-[জেলা] এর প্রদত্ত মৌখিক বিবৃতি কা: বি: ১৬১ ধারা মোতাবেক লিপিবদ্ধ করা হল।</p>
      <br>
      <p>আপনার জিজ্ঞাসাবাদে আমি জবানবন্দি দিচ্ছি যে, আমার নাম [নাম]। আমার বয়স [বয়স] বছর। আমি পেশায় [পেশা]। আমি অত্র মামলার [ভিকটিম/নিরপেক্ষ সাক্ষী/বাদীর ছেলে ইত্যাদি]। গত ইং- [তারিখ] তারিখ সকাল/বেলা অনুমান [সময়] ঘটিকায় আমি [কোথায় ছিলাম]। [এরপর বিরোধের কারণ, কে কী অস্ত্র নিয়ে আসলো, কে কাকে কোথায় আঘাত করলো বা সাক্ষী নিজে কী দেখলো তার বিস্তারিত বর্ণনা, এবং শেষে হাসপাতালে ভর্তি বা বিচার চাওয়ার কথা লিখবে]... আমি আসামীদের বিচার চাই। এই আমার জবানবন্দি।</p>
      <br><br>
      <p>ইহা আমার জবানবন্দি, আমাকে পড়িয়া শুনাইলে আমি শুদ্ধ মর্মে স্বীকার করিয়া সহি সম্পাদন করলাম।</p>
      <br><br><br>
      <p>_________________________</p>
      <p><b>সাক্ষীর স্বাক্ষর/টিপসহি</b></p>
      <p>তারিখ: [তদন্তের তারিখ বা ড্যাশ]</p>
      <br style="page-break-after: always;">
    `;

    const result = await model.generateContent(prompt);
    
    // ক্লিনআপ (মার্কডাউন রিমুভ)
    let statementText = result.response.text().replace(/```html/g, "").replace(/```/g, "");

    return NextResponse.json({ statement: statementText });

  } catch (error) {
    console.error("Error:", error);
    return NextResponse.json({ error: "সার্ভারে সমস্যা হয়েছে।" }, { status: 500 });
  }
}