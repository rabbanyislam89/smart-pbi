<p>বাদীর আনীত অভিযোগ পর্যালোচনা করা হয়েছে। ঘটনাস্থল পরিদর্শন করে ঘটনাস্থলের খসড়া মানচিত্র, সূচীপত্র ও ঘটনাস্থলের ছবি উত্তোলন করা হয়েছে। আলামত জব্দের চেষ্টা করা হলেও কোনো আলামত পাওয়া যায় নাই। কোনো দালিলিক সাক্ষ্য বা চিকিৎসা সনদ পাওয়া যায় নাই বিধায় পর্যালোচনা করা সম্ভব হয় নাই। বাদীর মানিত ০৩ জন সাক্ষীর সাক্ষ্য গ্রহণ করা হয়েছে। মামলাটির সার্বিক তদন্তে প্রাপ্ত সাক্ষ্য প্রমাণের ভিত্তিতে এবং ঘটনার পারিপার্শ্বিকতায় নিম্নোক্ত মতামত প্রদান করা হলো:</p>
